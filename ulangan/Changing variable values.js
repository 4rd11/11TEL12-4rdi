let jarakTempuh = 50;                 // Mendeklarasikan variabel jarakTempuh dengan nilai awal 100

console.log(jarakTempuh);           // -> 100 (Menampilkan nilai awal jarakTempuh)
jarakTempuh = 100;                 // Mengubah nilai jarakTempuh menjadi 120
console.log(jarakTempuh);         // -> 120 (Menampilkan nilai jarakTempuh yang sudah diubah)
jarakTempuh = jarakTempuh + 300; // Menambahkan 200 ke nilai jarakTempuh saat ini
console.log(jarakTempuh);       // -> 320 (Menampilkan hasil penambahan nilai jarakTempuh)